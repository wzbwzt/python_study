
'''
列表推导式:
快速创建列表的一种方式。

注意：
使用列表推导式生成的列表虽然十分方便，但是它生成的列表的所有元素会直接在内存中生成，当你要生成的列表元素个数十分巨大的时候，要注意小心你的内存。
'''


# 只有输出表达式和输入序列的基本列表推导式
list1 = [1, 2, 3]
print([x*10 for x in list1])  # 把list1中的每个元素乘以10

# 带有条件判断的列表推导式
print([x*10 for x in list1 if x % 2 != 0])  # 把list1中的每个不能被2整除的元素乘以10


# 多个输入序列的列表推导式
list1 = ["A", "B", "C"]
list2 = ["X", "Y", "Z"]
print([x+y for x in list1 for y in list2])  # 把list1和list2中的元素进行拼接
